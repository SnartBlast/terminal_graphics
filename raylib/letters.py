    def get_ord(self, letter):
        # Digits 0 - 9
        if (48 <= ord(letter) <= 57):
            return ord(letter) - 58

        # Special Symbols
        elif (letter == '!'):
            return 52
        elif (letter == '?'):
            return 53
        elif (letter == ' '):
            return 54
        elif (letter == '\''):
            return 55
        elif (letter == ','):
            return 56
        elif (letter == '.'):
            return 57
        elif (letter == '('):
            return 58
        elif (letter == ')'):
            return 59
        elif (letter == ':'):
            return 60
        elif (letter == ';'):
            return 61
        elif (letter == '<'):
            return 62
        elif (letter == '>'):
            return 63

        # Uppercase letters
        elif (ord(letter) > 90):
            return ord(letter) - 71

        # Lowercase letters
        else:
            return ord(letter) - 65

